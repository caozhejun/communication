package com.zhxjz.controller.form;

/**
 * easyUI分页对象
 * 
 * @author caozj
 * 
 */
public class EasyUIPageForm {

	/**
	 * 页码
	 */
	private int page;

	/**
	 * 每页记录数
	 */
	private int rows;

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

}
